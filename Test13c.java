class Print
{
	public static void main(String [] args)
	{
		int k=1;
		for(int n=5;n>=3;n--)
		{
			for(int i=1;i<=n;i++)
			{
				System.out.print(k);
			}
			k++;
			System.out.println();
		}
			int H=2;
		for(int j=2;j>=1;j--)
		{
			for(int i=1;i<=j;i++)
			{
				System.out.print(H);
			}
			H--;
			System.out.println();
		}
	}
}